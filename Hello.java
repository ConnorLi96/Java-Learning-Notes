public class Hello { // 类名是 Hello public 是访问修饰符，表示类是公开的
    public static void main(String[] args) { // 方法名是 main 
        // void 是返回类型，表示没有返回值
        // static 表示静态方法
        System.out.println("Hello, Java World!!!"); // 每一行语句必须以分号结束
    }
} // class 定义结束


